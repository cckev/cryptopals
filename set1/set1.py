"""
Determine which single character a hex string was xor'ed with 
"""
def find_xor_chr(hex_str):
	letters = set("ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz")
	candidates = []
	for i, key in enumerate(CHARS):
		score = 0
		desired_len = len(hex_str)
		candidate_key = key.replace("0x","") * (desired_len//2)
		candidate_str = xor_hex(hex_str, candidate_key).replace("0x","")

		if len(candidate_str) < desired_len:
			candidate_str = "0" * (desired_len-len(candidate_str)) + candidate_str
		try:
			candidate_str = bytes.fromhex(candidate_str).decode("utf-8")
		except:
			continue

		for c in candidate_str:
			if c in letters:
				score += FREQ_TABLE[c.upper()]
			elif c == " ":
				continue
			else:
				score -= 3.839 # penalize by subtracting average score in FREQ_TABLE

		candidates.append((candidate_str, score))

	
	return sorted(candidates,key=lambda x:x[1],reverse=True)[0]

